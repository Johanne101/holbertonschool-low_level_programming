#include "3-calc.h"

/**
 * op_add - returns sum.
 * @a: first integer
 * @b: second integer
 * Return: results
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - .function to subtract a & b.
 * @a: first integer
 * @b: second integer
 * Return: results
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - .function to multiply a & b.
 * @a: first integer
 * @b: second integer
 * Return: results
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - .function to divide a & b.
 * @a: first integer
 * @b: second integer
 * Return: results
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - .function to return remainder of a & b.
 * @a: first integer
 * @b: second integer
 * Return: results
 */

int op_mod(int a, int b)
{
	return (a % b);
}
